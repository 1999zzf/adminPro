<script>
let that;
export default {
  components: {},
  props: {},
  data() {
    return {
      showZJ: false,
      showBJ: false,
      showSC: false,
      showRef: false,
      bodyHeight: "",
      tableData: [],
      currentPage: 1,
      pageNum: 1, //当前页
      pageSize: 15, //每页显示条目数
      count: 0,

      zdkey: "",
      zdz: "",
      zdlx: "",

      formLabelWidth: "80px",
      dialogFormVisible: false,
      dialogFormVisible2: false,
      dialogFormVisible3: false,
      form: {
        dictionaryKey: "",
        dictionaryValue: "",
        dictionaryDes: "",
        dictionaryType: "",
        dictionaryTypeDes: "",
        rules: {
          dictionaryKey: [
            { required: true, message: "请输入字典key", trigger: "blur" },
          ],
          dictionaryValue: [
            { required: true, message: "请输入字典值", trigger: "blur" },
          ],
          dictionaryDes: [
            { required: true, message: "请输入字典描述", trigger: "blur" },
          ],
          dictionaryType: [
            { required: true, message: "请输入字典类型", trigger: "blur" },
          ],
          dictionaryTypeDes: [
            { required: true, message: "请输入类型描述", trigger: "blur" },
          ],
        },
      },
      form2: {
        dictionaryKey: "",
        dictionaryValue: "",
        dictionaryDes: "",
        dictionaryType: "",
        dictionaryTypeDes: "",
        rules: {
          dictionaryKey: [
            { required: true, message: "请输入字典key", trigger: "blur" },
          ],
          dictionaryValue: [
            { required: true, message: "请输入字典值", trigger: "blur" },
          ],
          dictionaryDes: [
            { required: true, message: "请输入字典描述", trigger: "blur" },
          ],
          dictionaryType: [
            { required: true, message: "请输入字典类型", trigger: "blur" },
          ],
          dictionaryTypeDes: [
            { required: true, message: "请输入类型描述", trigger: "blur" },
          ],
        },
      },

      thisRowId: "",
    };
  },
  watch: {},
  computed: {},
  methods: {
    getData() {
      that
        .$post("dictionary/getDictionaryPage", {
          pageSize: that.pageSize,
          currentPage: that.pageNum,
        })
        .then(function (data) {
          if (data && data.code == "0000") {
            that.count = data.page.count;
            that.tableData = data.page.list;
          } else {
            that.$message({
              message: data.msg,
              type: "warning",
            });
          }
        });
    },
    handleSizeChange(val) {
      that.pageSize = val;
      if (that.zdlx || that.zdkey || that.zdz) {
        that.search();
      } else {
        that.getData();
      }
    },
    handleCurrentChange(val) {
      that.pageNum = val;
      if (that.zdlx || that.zdkey || that.zdz) {
        that.search();
      } else {
        that.getData();
      }
    },

    showAddToast() {
      that.dialogFormVisible = true;
    },
    submitForm(form) {
      this.$refs[form].validate((valid) => {
        if (valid) {
          that
            .$post("dictionary/editDictionary", {
              dictionaryType: that.form.dictionaryType,
              dictionaryTypeDes: that.form.dictionaryTypeDes,
              dictionaryKey: that.form.dictionaryKey,
              dictionaryValue: that.form.dictionaryValue,
              dictionaryDes: that.form.dictionaryDes,
            })
            .then(function (data) {
              if (data && data.code == "0000") {
                that.$message({
                  message: "添加成功",
                  type: "success",
                });
                that.dialogFormVisible = false;
                that.form.dictionaryType = "";
                that.form.dictionaryTypeDes = "";
                that.form.dictionaryKey = "";
                that.form.dictionaryValue = "";
                that.form.dictionaryDes = "";
                that.getData();
              } else {
                that.$message({
                  message: data.msg,
                  type: "warning",
                });
              }
            });
        } else {
          console.log("error submit!!");
          return false;
        }
      });
    },

    handleClickDelete(row) {
      that
        .$confirm("删除该条数据, 是否继续?", "提示", {
          confirmButtonText: "确定",
          cancelButtonText: "取消",
          type: "warning",
        })
        .then(() => {
          that
            .$post("dictionary/delete", {
              id: row.id,
            })
            .then(function (data) {
              if (data && data.code == "0000") {
                that.$message({
                  message: "删除成功",
                  type: "success",
                });
                that.dialogFormVisible = false;
                if (that.zdlx || that.zdkey || that.zdz) {
                  that.search();
                } else {
                  that.getData();
                }
              } else {
                that.$message({
                  message: data.msg,
                  type: "warning",
                });
              }
            });
        })
        .catch(() => {});
    },
    handleClickEdit(row) {
      that.thisRowId = row.id;
      that.form2.dictionaryKey = row.dictionaryKey;
      that.form2.dictionaryValue = row.dictionaryValue;
      that.form2.dictionaryDes = row.dictionaryDes;
      that.form2.dictionaryType = row.dictionaryType;
      that.form2.dictionaryTypeDes = row.dictionaryTypeDes;
      that.dialogFormVisible2 = true;
    },
    submitForm2(form2) {
      this.$refs[form2].validate((valid) => {
        if (valid) {
          that
            .$post("dictionary/editDictionary", {
              id: that.thisRowId,
              dictionaryType: that.form2.dictionaryType,
              dictionaryTypeDes: that.form2.dictionaryTypeDes,
              dictionaryKey: that.form2.dictionaryKey,
              dictionaryValue: that.form2.dictionaryValue,
              dictionaryDes: that.form2.dictionaryDes,
            })
            .then(function (data) {
              if (data && data.code == "0000") {
                that.$message({
                  message: "修改成功",
                  type: "success",
                });
                that.dialogFormVisible2 = false;
                that.form.dictionaryType = "";
                that.form.dictionaryTypeDes = "";
                that.form.dictionaryKey = "";
                that.form.dictionaryValue = "";
                that.form.dictionaryDes = "";
                if (that.zdlx || that.zdkey || that.zdz) {
                  that.search();
                } else {
                  that.getData();
                }
              } else {
                that.$message({
                  message: data.msg,
                  type: "warning",
                });
              }
            });
        } else {
          console.log("error submit!!");
          return false;
        }
      });
    },
    showSearchToast() {
      that.pageNum = 1;
      that.currentPage = 1;
      that.search();
    },
    search() {
      that
        .$post("dictionary/getDictionaryPage", {
          pageSize: that.pageSize,
          currentPage: that.pageNum,
          dictionaryType: that.zdlx,
          dictionaryKey: that.zdkey,
          dictionaryValue: that.zdz,
        })
        .then(function (data) {
          if (data && data.code == "0000") {
            that.count = data.page.count;
            that.tableData = data.page.list;
          } else {
            that.$message({
              message: data.msg,
              type: "warning",
            });
          }
        });
    },
    clearAll() {
      that.zdlx = "";
      that.zdkey = "";
      that.zdz = "";
      that.pageNum = 1;
      that.currentPage = 1;
      that.getData();
    },
  },
  created() {},
  mounted() {
    that = this;
    that.bodyHeight = window.innerHeight - 124;
    // that.isRule = JSON.parse(sessionStorage.getItem("rule"));
    // if (that.isRule) {
    //   for (var t = 0; t < that.isRule.length; t++) {
    //     if (that.isRule[t].menuName == "增加") {
    //       that.showZJ = true;
    //     }
    //     if (that.isRule[t].menuName == "删除") {
    //       that.showSC = true;
    //     }
    //     if (that.isRule[t].menuName == "编辑") {
    //       that.showBJ = true;
    //     }
    //   }
    // }
    that.asideData = JSON.parse(sessionStorage.getItem("asideData"));
    if (that.asideData) {
      for (var ti = 0; ti < that.asideData.length; ti++) {
        for (var tis = 0; tis < that.asideData[ti].chrildList.length; tis++) {
          if (that.asideData[ti].chrildList[tis].pageUrl == "dictionaryManage")
            for (
              var tiss = 0;
              tiss < that.asideData[ti].chrildList[tis].buttonList.length;
              tiss++
            ) {
              if (
                that.asideData[ti].chrildList[tis].buttonList[tiss].menuName ==
                "增加"
              ) {
                that.showZJ = true;
              }
              if (
                that.asideData[ti].chrildList[tis].buttonList[tiss].menuName ==
                "删除"
              ) {
                that.showSC = true;
              }
              if (
                that.asideData[ti].chrildList[tis].buttonList[tiss].menuName ==
                "编辑"
              ) {
                that.showBJ = true;
              }
            }
        }
      }
    }
    that.getData();
  },
};
</script>
